//increment
let z =50;
//post increment

console.log(z++); //z=z+1 =>51
console.log(z); //51

//pre increment
console.log(++z); //z=z+1 =>52

z +=20 ;
console.log(z);

// decrement
let x =80;
//post decrement

console.log(x--); //x=x-1 =>79
console.log(x); //79

//pre decrement
console.log(--x); //x=x-1 =>75

x -=20 ;
console.log(x);

//logical operators
console.log(true || true); //true
console.log(true || false); //true
console.log(false || true);  //true
console.log(false || false); //false

console.log ("and case");

